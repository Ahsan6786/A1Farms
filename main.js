let currentLang = localStorage.getItem('a1_farms_lang');

const updateContent = () => {
    if (!currentLang) {
        document.getElementById('language-modal').classList.add('active');
        return;
    }
    
    document.querySelectorAll('[data-i18n]').forEach(el => {
        const key = el.getAttribute('data-i18n');
        if (translations[currentLang][key]) {
            el.innerHTML = translations[currentLang][key];
        }
    });
    document.documentElement.lang = currentLang;
    const langBtn = document.querySelector('.lang-toggle');
    if (langBtn) {
        langBtn.textContent = currentLang === 'en' ? 'हिन्दी' : 'English';
    }
};

// Language Selection from Modal
document.addEventListener('click', (e) => {
    if (e.target.classList.contains('lang-select')) {
        currentLang = e.target.getAttribute('data-lang');
        localStorage.setItem('a1_farms_lang', currentLang);
        document.getElementById('language-modal').classList.remove('active');
        updateContent();
    }
    
    if (e.target.classList.contains('lang-toggle')) {
        currentLang = currentLang === 'en' ? 'hi' : 'en';
        localStorage.setItem('a1_farms_lang', currentLang);
        updateContent();
    }
});

// Initial Load
document.addEventListener('DOMContentLoaded', () => {
    // Check if language is already set
    if (!localStorage.getItem('a1_farms_lang')) {
        document.getElementById('language-modal').classList.add('active');
    }
    
    updateContent();
    // Re-check reveal after content update
    setTimeout(revealOnScroll, 100);
});

// Advanced Reveal On Scroll
const revealOnScroll = () => {
    const reveals = document.querySelectorAll('.reveal');
    reveals.forEach(el => {
        const windowHeight = window.innerHeight;
        const revealTop = el.getBoundingClientRect().top;
        const revealPoint = 120; // Slightly more offset for better feel

        if (revealTop < windowHeight - revealPoint) {
            el.classList.add('active');
        }
    });
};

window.addEventListener('scroll', revealOnScroll);
window.addEventListener('load', revealOnScroll);

// Header scroll effect
const header = document.querySelector('header');
window.addEventListener('scroll', () => {
    if (window.scrollY > 50) {
        header.classList.add('scrolled');
    } else {
        header.classList.remove('scrolled');
    }
});

// Mobile Menu Toggle
const menuToggle = document.querySelector('.menu-toggle');
const navLinks = document.querySelector('.nav-links');

if (menuToggle) {
    menuToggle.addEventListener('click', () => {
        navLinks.classList.toggle('active');
        menuToggle.classList.toggle('active');
    });
}

// Smooth scroll
document.querySelectorAll('a[href^="#"]').forEach(anchor => {
    anchor.addEventListener('click', function (e) {
        e.preventDefault();
        const targetId = this.getAttribute('href');
        const targetElement = document.querySelector(targetId);
        if (targetElement) {
            window.scrollTo({
                top: targetElement.offsetTop - 80,
                behavior: 'smooth'
            });
            navLinks.classList.remove('active');
        }
    });
});
